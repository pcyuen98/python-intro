# Introduction to Datatypes
# we will name variables as one, two, three...

# number datatype

one = 1   # integer
print ('one -->',one)

two = 1.2   # float
print ('two -->', two)
       
# NONE datatype
four = None
print ('four -->', four);

# string datatype
five = "We Love Study Today"
print ('five -->',five);

# list datatype - Can Change!
six = [1, 2, 4, 8]
print ('six -->',six);

sixFirstValue = six[0] = 999
print ('sixFirstValue -->',sixFirstValue); 
print ('six after changed -->',six); 

# tuples 
seven = ("a", "e", "i", "o", "u")
print ('seven -->',seven);

